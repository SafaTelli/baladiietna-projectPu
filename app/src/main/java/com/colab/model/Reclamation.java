package com.colab.model;

public class Reclamation {

    String type , description , lieu , date, state, reclamationImage  ;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLieu() {
        return lieu;
    }

    public void setLieu(String lieu) {
        this.lieu = lieu;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getReclamationImage() {
        return reclamationImage;
    }

    public void setReclamationImage(String reclamationImage) {
        this.reclamationImage = reclamationImage;
    }
}
